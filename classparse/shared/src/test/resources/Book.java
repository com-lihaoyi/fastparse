class Book {
    private String title;
    private int pubYear;

    public String getTitle() {
        return title;
    }

    public int getPubYear() {
        return pubYear;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setPubYear(int pubYear) {
        this.pubYear = pubYear;
    }
}

